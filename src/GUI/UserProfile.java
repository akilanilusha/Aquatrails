    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
            String gender;
            query = "insert into User_Info values(?, ?, ?, ?, ?, ?, ?, ?)";
            statement = connection.prepareStatement(query);
            statement.setString(1, txtUserid.getText());
            statement.setString(2, txtName.getText());
            
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Object date = null;
            statement.setString(3, sdf.format(date));

            statement.setInt(4, Integer.parseInt(txtContactno.getText()));
            
            if(rbMale.isSelected())
                gender = "Male";
            else
                gender = "Female";
            
            statement.setString(5, gender);
            statement.setString(6, cmbUserrole.getSelectedItem().toString());
            statement.setString(7, txtEmail.getText());
            statement.setString(8, cmbNationality.getSelectedItem().toString());
            
            int rows = statement.executeUpdate();
            
            if(rows > 0){
                System.out.println(rows + "row(s) affected");
                JOptionPane.showMessageDialog(this, "Record Added Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
            statement.close();
            
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            String gender;
            query = "update User_Info set name = ?, dob = ?, contactNo = ?, gender = ?, userrole = ?, email = ?, nationality = ? where userId = ?";
            statement = connection.prepareStatement(query);
            statement.setString(1, txtName.getText());
            java.util.Date selectedDate = jDateChooser1.getDate();
            if (selectedDate != null) {
                java.sql.Date sqlDate = new java.sql.Date(selectedDate.getTime());
                statement.setDate(2, sqlDate);
            } else {
                JOptionPane.showMessageDialog(this, "Please select a date of birth.");
                return;
            }
            statement.setInt(3, Integer.parseInt(txtContactno.getText()));
            if(rbMale.isSelected())
                gender = "Male";
            else
                gender = "Female";
            statement.setString(4, gender);
            statement.setString(5, cmbUserrole.getSelectedItem().toString());
            statement.setString(6, txtEmail.getText());
            statement.setString(5, cmbNationality.getSelectedItem().toString());
            
            int rows = statement.executeUpdate();
            if(rows > 0){
                System.out.println(rows + "row(s) affected.");
                JOptionPane.showMessageDialog(this, "Record Updated Successfully", "Update", JOptionPane.INFORMATION_MESSAGE);
            }
            //updateTable();
            statement.close();
            clear();
        }catch(SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

