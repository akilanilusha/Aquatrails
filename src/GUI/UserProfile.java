    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try {
            query = "select * from User_Info where userId = ?";
            statement = connection.prepareStatement(query);
            statement.setString(1, txtUserid.getText());
            ResultSet rs = statement.executeQuery();
            
            if(rs.next()){
                txtName.setText(rs.getString("name"));
                Date dob = rs.getDate("dob");
                jDateChooser1.setDate(dob);
                txtContactno.setText(Integer.toString(rs.getInt("contactNo")));
                if("Male".equals(rs.getString("gender")))
                    rbMale.setSelected(true);
                else
                    rbFemale.setSelected(true);
                
                cmbUserrole.setSelectedItem(rs.getString("userrole"));
                txtEmail.setText(rs.getString("email"));
                cmbNationality.setSelectedItem(rs.getString("nationality"));
                statement.close();
            }else{
                JOptionPane.showMessageDialog(this, "Can not find the record", "Error", JOptionPane.ERROR_MESSAGE);
                clear();
            }
        }catch(SQLException ex){
            ex.getStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

